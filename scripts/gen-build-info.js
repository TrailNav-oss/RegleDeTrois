#!/usr/bin/env node
const fs = require("fs");
const path = require("path");
const { execSync } = require("child_process");

const root = path.resolve(__dirname, "..");
const appJson = JSON.parse(fs.readFileSync(path.join(root, "app.json"), "utf-8"));
const outputPath = path.join(root, "src", "config", "buildInfo.ts");

const version = appJson.expo.version;
const build = appJson.expo.android.versionCode;
const date = new Date().toISOString();

let gitHash = "unknown";
let branch = "unknown";
try {
  gitHash = execSync("git rev-parse --short HEAD", { cwd: root }).toString().trim();
  branch = execSync("git rev-parse --abbrev-ref HEAD", { cwd: root }).toString().trim();
} catch {
  // Not in a git repo or git not available
}

const content = `// Auto-generated by scripts/gen-build-info.js — do not edit
export const BUILD_INFO = {
  version: "${version}",
  build: ${build},
  gitHash: "${gitHash}",
  branch: "${branch}",
  date: "${date}",
} as const;
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, content, "utf-8");

console.log(`  buildInfo.ts generated:`);
console.log(`    version:  ${version}`);
console.log(`    build:    ${build}`);
console.log(`    gitHash:  ${gitHash}`);
console.log(`    branch:   ${branch}`);
console.log(`    date:     ${date}`);
